package ex_13072024;

public class Lab027 {
    public static void main(String[] args) {

        /*Ternary Operator*/
        /*When more than two Operands were used to do Arithmetic operation then it is Ternary Operator*/
        int a=10;
        int b=10;
        int c=10;
        int d=a+b+c;

        // a is one Operand
        // b is an Operand
        // c is an Operand
        System.out.println(d);
    }
}
