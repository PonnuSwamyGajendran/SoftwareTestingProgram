package ex_13072024;

public class Lab028 {
    public static void main (String[] args){
        /*Relational Operator*/
        /* > , < , <= , >=, ==, != */
        /*Its outcome is always boolean*/

        int a=10;
        int b=20;
        System.out.println(a>b); // False
        System.out.println(a<b); // T
        System.out.println(a>=b); // F
        System.out.println(a<=b); // T
        System.out.println(a==b); // F
        System.out.println(a!=b); // T //not equal to [It comes under logical operator]
        // '=' is used to assign value/ assignment operator where '==' is used to compare 2 values
    }
}
