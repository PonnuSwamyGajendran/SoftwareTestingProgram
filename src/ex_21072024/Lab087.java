package ex_21072024;

public class Lab087 {
    public static void main(String[] args) {
        // Use of break and continue
        // Break ---> come of out loop
        // Continue ---> skip the next step and again go to loop
        for(int i=0; i<=10; i++){
            if (i==6){
                break;
            }
            else{
                System.out.println(i);
            }
        }
    }
}
