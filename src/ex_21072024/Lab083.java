package ex_21072024;

public class Lab083 {
    public static void main(String[] args) {
        // find even numbers in the range 0 to 100
        for(int i=0; i<=100;i++){
            if(i%2==0){
                System.out.println("Even number ---> "+i);
            }
            else{
                System.out.println(i);
            }
        }
    }
}
